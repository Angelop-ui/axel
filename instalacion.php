  <h2>Instalación del Sistema de Captación de Agua Pluvial</h2>
  <p>La instalación de un sistema de captación de agua pluvial es sencilla, económica y altamente funcional. A continuación, se detallan los pasos clave:</p>

  <ol>
    <li><strong>Ubicación:</strong> Identifica una superficie inclinada del techo o azotea donde naturalmente se concentre el flujo de agua durante las lluvias. Evita zonas debajo de árboles o expuestas a contaminantes.</li>
    <li><strong>Instalación de canaletas:</strong> Coloca canaletas o canalones de PVC o aluminio en el borde inferior del techo. Asegúrate de que tengan una inclinación mínima del 2% para facilitar el flujo del agua hacia el bajante.</li>
    <li><strong>Conexión al bajante:</strong> Dirige el agua captada hacia un tubo de bajada conectado a un filtro de hojas o rejilla. Este tubo debe conducir directamente al sistema de almacenamiento.</li>
    <li><strong>Filtración primaria:</strong> Instala un filtro tipo malla, arena o grava para retener hojas, tierra, insectos y residuos. También puedes usar un separador de primeras lluvias para desechar el agua más sucia al inicio.</li>
    <li><strong>Almacenamiento:</strong> Utiliza un tinaco, tanque, bidón o cisterna con tapa hermética para evitar la proliferación de mosquitos. El depósito debe estar limpio y ubicado en sombra si es posible.</li>
    <li><strong>Purificación (opcional):</strong> Si el agua se usará para consumo humano, se recomienda incluir sistemas de filtración por carbón activado, luz UV o cloración.</li>
    <li><strong>Distribución (opcional):</strong> Puedes conectar el sistema a bombas o redes de riego para distribuir el agua en tu hogar, jardín o cultivo.</li>
    <li><strong>Mantenimiento regular:</strong> Revisa canaletas, filtros y conexiones cada mes. Limpia residuos, revisa que no haya fugas ni grietas, y asegúrate de que no haya estancamiento de agua.</li>
  </ol>

  <p><strong>Consejo:</strong> Antes de iniciar, dibuja un esquema del sistema y toma en cuenta la pendiente, materiales disponibles y uso final del agua. ¡Así evitarás errores y maximizarás el rendimiento!</p>

