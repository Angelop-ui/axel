<section class="ventajas">
  <h2>Ventajas</h2>

  <ul>
    <li><strong>Ahorro de agua potable:</strong> reduce el uso del suministro público, sobre todo en actividades no potables como riego o limpieza.</li>
    <li><strong>Reducción de inundaciones:</strong> al captar el agua desde los techos, se evita su acumulación en calles y drenajes, disminuyendo riesgos de encharcamiento.</li>
    <li><strong>Autonomía:</strong> permite contar con una fuente alternativa de agua en caso de escasez o cortes del servicio.</li>
    <li><strong>Costos menores:</strong> reduce el consumo del medidor domiciliario, bajando el costo en el recibo del agua.</li>
    <li><strong>Recarga de mantos acuíferos:</strong> al permitir la infiltración controlada del excedente captado.</li>
    <li><strong>Adaptación al cambio climático:</strong> es una estrategia sustentable ante el aumento de sequías e irregularidad de lluvias.</li>
    <li><strong>Uso educativo:</strong> promueve la conciencia ambiental en familias, comunidades y escuelas.</li>
    <li><strong>Instalación flexible:</strong> puede colocarse en casas, edificios, escuelas, invernaderos, etc.</li>
  </ul>

